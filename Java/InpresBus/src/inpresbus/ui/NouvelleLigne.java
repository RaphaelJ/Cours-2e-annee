    private void butSupprimerArretActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butSupprimerArretActionPerformed
        if (this.comboArrets.getSelectedObjects().length > 0) {
            String nom = (String) this.comboArrets.getSelectedObjects()[0];
            int index = this._arrets.indexOf(nom);
            this._arrets.remove(index);
            
            DefaultTableModel mTable = (DefaultTableModel) this.tableArrets.getModel();
            mTable.removeRow(index);
            
            DefaultComboBoxModel mCombo = (DefaultComboBoxModel) this.comboArrets.getModel();
            mCombo.removeElementAt(index);
        } else {
            this.setError("Aucun arrêt à supprimer");
        }
    }//GEN-LAST:event_butSupprimerArretActionPerformed

    private void butConfirmerArretsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butConfirmerArretsActionPerformed
        if (this._arrets.size() >= 2) {
            this.setEtape3Enabled(true);
            this.labelArret1.setText(this._arrets.get(0));
            this.labelArret2.setText(this._arrets.get(1));
            
            if (this._arrets.size() < 3)
                this.butArretSuivant.setText("Confirmer");
            else
                this.butArretSuivant.setText("Suivant");
        } else {
            this.setError("Vous devez au moins entrer deux arrêts");
        }
    }//GEN-LAST:event_butConfirmerArretsActionPerformed

    private void butArretSuivantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butArretSuivantActionPerformed
        try {
            this._distances.add(Float.parseFloat(this.textDistance.getText()));
        } catch (NumberFormatException e) {
            this.setError("Distance non valide");
            
            return;
        }
        
        if (this._distances.size() == this._arrets.size() - 1) {
            // Dernier arret, enregistre les arrets et quitte la fenêtre
            
            Segment[] segments = new Segment[this._distances.size()];
            
            int i = 0;
            for (float distance : this._distances) {
                segments[i] = new Segment(
                        this._arrets.get(i), this._arrets.get(i+1),
                        distance
                );
                    
                i++;
            }
            
            this.getLigne().setSegments(segments);
            
            try {
                Ligne.dir.enregistrerObjet(
                    this.getLigne(),
                    this.getLigne().getNom()
                );
            } catch (Exception ex) {
                System.err.println(
                    "Impossible d'enregistrer la ligne: " + ex.getMessage()
                );
            }
            
            this.dispose();
        } else {
            if (this._distances.size() == this._arrets.size() - 2) {
                // Avant-dernier arret
                this.butArretSuivant.setText("Confirmer");
                
                
            } else if (this._distances.size() < this._arrets.size() - 1) {
                // Arrets intermédiares
                this.butArretSuivant.setText("Suivant");
            }
        
            DefaultTreeModel mTree = (DefaultTreeModel) this.treeArrets.getModel();
            DefaultMutableTreeNode rootNode = (DefaultMutableTreeNode) mTree.getRoot();

            DefaultMutableTreeNode arretNode = new DefaultMutableTreeNode(
                    this._arrets.get(this._distances.size() - 1)
                    + " - " +
                    this._arrets.get(this._distances.size())
            );
            DefaultMutableTreeNode distanceNode = new DefaultMutableTreeNode(
                    this._distances.getLast().toString()
            );
            arretNode.add(distanceNode);
            rootNode.add(arretNode);
            
            mTree.reload();
            
            this.labelArret1.setText(this._arrets.get(this._distances.size()));
            this.labelArret2.setText(this._arrets.get(this._distances.size() + 1));
        }
    }//GEN-LAST:event_butArretSuivantActionPerformed
    
