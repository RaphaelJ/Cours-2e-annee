    private void butAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butAjouterActionPerformed
        String nom = this.textNomArret.getText();
        if (this._arrets.contains(nom)) 
            this.labelErreur.setText("Ce nom d'arret existe déjà");
        else {
            this._arrets.add(nom);
        
            Object[] objs = new Object[] { nom };
            DefaultTableModel mTable = (DefaultTableModel) this.tableArrets.getModel();
            mTable.addRow(objs);
        
            DefaultComboBoxModel mCombo = (DefaultComboBoxModel) this.comboArrets.getModel();
            mCombo.addElement(nom);
        }
    }//GEN-LAST:event_butAjouterActionPerformed

    private void butCreerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butCreerActionPerformed
        try {
            String nom = this.textNumLigne.getText();
            
            if (Ligne.dir.objetExistant(nom)) {
                this.setError("Cette ligne existe déjà");
            } else {
                this._ligne = new Ligne(
                        Integer.parseInt(nom),
                        nom,
                        null
                );
            
                this.setEtape2Enabled(true);
            }
        } catch (Exception e) {
            this.setError("Numero de ligne non valide");
        }
    }//GEN-LAST:event_butCreerActionPerformed
    
