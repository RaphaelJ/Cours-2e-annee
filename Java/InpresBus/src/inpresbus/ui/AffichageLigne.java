    private void afficherLigne(Ligne ligne)
    {
        DefaultTreeModel mTree = (DefaultTreeModel) this.treeArrets.getModel();
        DefaultMutableTreeNode rootNode = (DefaultMutableTreeNode) mTree.getRoot();

        for (Segment seg : ligne.getSegments()) {
            DefaultMutableTreeNode arretNode = new DefaultMutableTreeNode(
                seg.getDepart()
                + " - " +
                seg.getArrivee()
            );
            DefaultMutableTreeNode distanceNode = new DefaultMutableTreeNode(
                seg.getDuree()
            );
            arretNode.add(distanceNode);
            rootNode.add(arretNode);
        }
            
        mTree.reload();
    }

